$mapping = [byte[]] (
  0x00, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00,
  0x04, 0x00, 0x00, 0x00,  # 3 mapping + 1 ending  = 0x04
  0x01, 0x00, 0x3A, 0x00,  # 1st mapping: Caps Lock to Escape
  0x3A, 0x00, 0x01, 0x00,  # 2nd mapping: Escape to Casp Lock
  0x1D, 0xE0, 0x5C, 0xE0,  # 3rd mapping: Left Windows to Left Control
  0x00, 0x00, 0x00, 0x00   # final four bytes
)

new-itemProperty -path "HKLM:\SYSTEM\CurrentControlSet\Control\Keyboard Layout" -name "Scancode Map" -value $mapping -force
